    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String clave = "";
        for(char a:txt_clave.getPassword()){
            clave += a;
        }
        if(Utilidades.requerido(txt_usuario, txt_usuario.getText())&&Utilidades.requerido(txt_clave, clave)){
            Session.setCuenta(sc.inicio(txt_usuario.getText(), clave));
            if(Session.getCuenta()!=null){
                new MenuPrincipalAdministrador();
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "No esta registrado en el sistema o su clave o usaurio estan mal ingresados");
                this.txt_clave.setText("");
                this.txt_usuario.setText("");
            }
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

