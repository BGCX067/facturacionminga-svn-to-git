    private void cargarObjeto() {
        sp.getLoteProducto().setCodBarra_lote(txt_codigo.getText());
        sp.getLoteProducto().setNro_compra(txt_nroventa.getText());
        sp.getLoteProducto().setPc_lote(Double.parseDouble(txt_precio.getText()));
        sp.getLoteProducto().setPv_lote(Double.parseDouble(txt_precioVenta.getText()));        
        sp.getLoteProducto().setStock(Integer.parseInt(txt_stock.getText()));
        sp.getLoteProducto().setProducto(FacadeComponentes.obtenerProductoCombo(cbx_producto));
    }

    private void limpiaCampos() {
        txt_codigo.setText("");
        txt_nroventa.setText("");
        txt_precio.setText("");
        txt_precioVenta.setText("");
        txt_stock.setText("");        
        txt_utilidad.setText("");
        cbx_producto.setSelectedIndex(0);
        sp.nuevaInstancia();
    }

    private void cargarVista() {
        txt_codigo.setText(sp.getLoteProducto().getCodBarra_lote());
        txt_nroventa.setText(sp.getLoteProducto().getNro_compra());
        txt_precio.setText(sp.getLoteProducto().getPc_lote()+"");
        txt_precioVenta.setText(sp.getLoteProducto().getPv_lote()+"");
        txt_stock.setText(sp.getLoteProducto().getStock()+"");
        double utilidad = ((sp.getLoteProducto().getPv_lote()-sp.getLoteProducto().getPc_lote())/sp.getLoteProducto().getPc_lote())*100;
        txt_utilidad.setText(utilidad+"");
        cbx_producto.setSelectedItem(sp.getLoteProducto().getProducto());
    }
    private void cargarDatosProducto(){
        txt_producto.setText("");
        ServicioProducto aux = new ServicioProducto();
        aux.fijarInstancia(FacadeComponentes.obtenerProductoCombo(cbx_producto));
        txt_producto.setText("Tipo: "+aux.getProducto().getTipo()+"\n"+"Marca: "+aux.getProducto().getMarca()+"\n"+
                "Modelo: "+aux.getProducto().getModelo()+"\n"+"Categoría: "+aux.getProducto().getCategoria()+"\n"+"Descripción: "+aux.getProducto().getDescripcion()+"\n");
    }
    private void calcularPv(){
        double precioc = 0;
        double utilidad=0;
        double suma=0;
        double pvp=0;
        try {
            precioc = Double.parseDouble(txt_precio.getText());
            utilidad = Double.parseDouble(txt_utilidad.getText());
        } catch (Exception e) {
        }
        suma = (precioc*utilidad)/100;
        pvp = precioc+suma;
        txt_precioVenta.setText(String.valueOf(pvp));
    }
