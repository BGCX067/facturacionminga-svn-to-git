    private void buscar(){        
        String criterio = "";
        String texto = txtBusquedaProducto.getText();
        if(cmbCriterioProducto.getSelectedIndex()==0){
            cargarTabla();
        }else{
            if(cmbCriterioProducto.getSelectedIndex()==3){
                criterio=" p.producto.categoria.nombre_categoria";
            }
            if(cmbCriterioProducto.getSelectedIndex()==2){
                criterio=" p.producto.tipo.nombre_tipo";
            }
            if(cmbCriterioProducto.getSelectedIndex()==4){
                criterio=" p.producto.marca.nombre_marca";
            }
            if(cmbCriterioProducto.getSelectedIndex()==1){
                criterio=" p.codBarra_lote";
            }
            modelo.setLista(sp.buscar(criterio, texto));
            tbl_tabla.updateUI();
        }
        
    }

