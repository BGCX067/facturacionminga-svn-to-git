    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(Utilidades.requerido(txt_codigo, txt_codigo.getText())==true&&
            Utilidades.requerido(txt_precio, txt_precio.getText())==true&&
                Utilidades.requerido(txt_precioVenta, txt_precioVenta.getText())==true&&
                Utilidades.requerido(txt_stock, txt_stock.getText())==true){
            cargarObjeto();
            if(sp.getLoteProducto().getId_lote()==null){
                if(sp.guardar()==true){
                    JOptionPane.showMessageDialog(this, "Se ha guardado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                    cargarTabla();
                    irListar();
                    limpiaCampos();
                }else{
                    JOptionPane.showMessageDialog(this, "No se ha guardado", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                if(sp.guardar()==true){
                    JOptionPane.showMessageDialog(this, "Se ha modificado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                    cargarTabla();
                    irListar();
                    limpiaCampos();
                }else{
                    JOptionPane.showMessageDialog(this, "No se ha modificado", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarProductoActionPerformed
        // TODO add your handling code here:
        int pos = tbl_tabla.getSelectedRow();
        if (pos >= 0) {
            sp.fijarInstancia(modelo.getLista().get(pos));
            irNuevo();
            cargarVista();
            
            tbl_tabla.clearSelection();
            tabbed.setTitleAt(0, "MODIDICAR");
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un lote", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarProductoActionPerformed

