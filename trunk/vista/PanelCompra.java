    private void cargarVista() {
        if (sv.getCompra().getArchivo() != null) {
            dir = sv.getCompra().getArchivo().getNombre_arch();
            txt_factura.setText(dir);
        }
    }

    private void subirArchivo() {
        if (dir.isEmpty() == false) {
            Utilidades.copiararchivos(dir, Utilidades.obtenerRutaRoot() + File.separatorChar + "compras" + File.separatorChar + this.sv.getCompra().getId_compra().intValue() + "-compra.pdf");
            ServicioArchivo sa = new ServicioArchivo();
            sa.fijarInstancia(sv.getCompra().getArchivo());
            sa.getArchivo().setDireccion_arch(Utilidades.obtenerRutaRoot() + File.separatorChar + "compras" + File.separatorChar);
            sa.getArchivo().setNombre_arch(this.sv.getCompra().getId_compra().intValue() + "-compra.pdf");
            sa.getArchivo().setCompra(sv.getCompra());
            sv.getCompra().setArchivo(sa.getArchivo());
        } else {
            JOptionPane.showMessageDialog(this, "No ha cargado el archivo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void enviarDatosReporte(ServicioVenta sva) {
        HashMap parametros = new HashMap();
        parametros.put("nro_fact", sva.getVenta().getNro_venta());
        parametros.put("fecha", Utilidades.formatoFechaDateString(sva.getVenta().getFecha_venta()));
        parametros.put("cedula", sva.getVenta().getCliente().getCedula_per());
        parametros.put("cliente", sva.getVenta().getCliente().toString());
        parametros.put("direccion", sva.getVenta().getCliente().getDireccion_per());
        parametros.put("fono", sva.getVenta().getCliente().getFono_per());
        parametros.put("subtotal", sva.getVenta().getSubtotal());
        parametros.put("iva", sva.getVenta().getIva());
        parametros.put("descuento", 0.0);
        parametros.put("total", (sva.getVenta().getTotal()));
        new Reporte().crearReporte("factura", sva.getVenta().getListaDetalle(), "factura.pdf", parametros);
    }

    private void crearCuentasTablas() {
        modeloCuotas.getLista().clear();
        String tipo = cbx_tipo.getSelectedItem().toString();
        if (!tipo.equalsIgnoreCase("contado")) {
            double total = 0;
            try {
                total = Double.parseDouble(txtTotal.getText());
            } catch (Exception e) {
            }
            if (total > 0) {
                double abono = 0;
                try {
                    abono = Double.parseDouble(txt_abono.getText());
                } catch (Exception e) {
                }
                double saldo = total - abono;
                int nro = Integer.parseInt(cbx_nro.getSelectedItem().toString());
                ServicioCuotaCobrarPagar sccp = new ServicioCuotaCobrarPagar();
                sccp.getCuota().setAbono(abono);
                sccp.getCuota().setSaldo_actual(saldo);
                sccp.getCuota().setNro_cuotas(nro);
                sccp.getCuota().setTipo("venta");
                sccp.getCuota().setCompra(this.sv.getCompra());
                this.sv.getCompra().setCuentapc(sccp.getCuota());

                modeloCuotas.setLista(FacadeComponentes.crearCuotas(saldo, nro, sccp.getCuota(), tipo));
                tbl_tablaCuotas.updateUI();
            }
        } else {
            bloquear();
        }
    }

    private void bloquear() {
        txt_abono.setText("0");
        cbx_nro.setSelectedIndex(0);
        modeloCuotas.getLista().clear();
        tbl_tablaCuotas.updateUI();
    }
