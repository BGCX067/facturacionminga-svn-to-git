    private void sumar() {
        double subtotal = 0;
        double iva = 0;
        double descuento = 0;
        double total = 0;
        try {
            subtotal = Double.parseDouble(txtSubtotal.getText());
            iva = Double.parseDouble(txtIva.getText());
            descuento = Double.parseDouble(txt_descuento.getText());
            
        } catch (Exception e) {
        }
        total = subtotal + iva - descuento;
        txtTotal.setText(String.valueOf(total));
    }

