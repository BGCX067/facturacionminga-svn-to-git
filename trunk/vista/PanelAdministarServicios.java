    private void buscar(){
        /*Todos
Por Categoría
Por Tipo
Por Marca
Por Modelo*/
        String criterio = "";
        String texto = txtBusquedaProducto.getText();
        if(cmbCriterioProducto.getSelectedIndex()==0){
            cargarTabla();
        }else{
            if(cmbCriterioProducto.getSelectedIndex()==1){
                criterio=" p.nombre_serv";
            }            
            modelo.setLista(sp.buscar(criterio, texto));
            tbl_tabla.updateUI();
        }
        
    }

