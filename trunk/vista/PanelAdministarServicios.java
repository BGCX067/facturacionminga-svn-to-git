    private void btnModificarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarProductoActionPerformed
        // TODO add your handling code here:
        int pos = tbl_tabla.getSelectedRow();
        if (pos >= 0) {
            sp.fijarInstancia(modelo.getLista().get(pos));
            cargarVista();
            irNuevo();
            tbl_tabla.clearSelection();
            tabbed.setTitleAt(0, "MODIDICAR");
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un lote", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarProductoActionPerformed

    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        // TODO add your handling code here:
        int pos = tbl_tabla.getSelectedRow();
        if (pos >= 0) {
            sp.fijarInstancia(modelo.getLista().get(pos));
            //cargarVista();
            //irNuevo();
            tbl_tabla.clearSelection();
            //tabbed.setTitleAt(0, "MODIDICAR");
            String aux = (sp.getServicio().getEstado_serv().equalsIgnoreCase("En uso"))?"Desabilitado":"En uso";
            sp.getServicio().setEstado_serv(aux);
            sp.guardar();
            JOptionPane.showMessageDialog(this, "Se ha actualizado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            limpiaCampos();
            cargarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un Porducto", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

